// JavaScript Document
function imageEnlarge(flyer) {
	
 document.getElementById("flyer").style.height="100%";
 document.getElementById("flyer").style.width="100%";
 document.getElementById("flyer").style.position='absolute';
 }

function imageReset(flyer) {

 document.getElementById("flyer").style.height="80%";
 document.getElementById("flyer").style.width="80%";
 document.getElementById("flyer").style.position='absolute';
 }
